
public class Account {
	private String name;
	private int balance;
	
	// 접근자 , getters
	public String getName() {
		return name;
	}
	public int getBalance() {
		return balance;
	}
	
	// setters, 설정자
	public void setName(String name) {
		this.name = name;
	}
	public void setBalance(int balance) {
		this.balance = balance;
	}
}
